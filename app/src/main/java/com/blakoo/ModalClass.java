package com.blakoo;

public class ModalClass {

    //** ModalClass for slideshow in home page.

    int image;
    String title;

    public ModalClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
